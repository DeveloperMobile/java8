
package cap02;

/** Exemplo Comentários:
 * Essa classe demonstra a utilização de variáveis em uma classe
 * em Java. São declaradas varivávies do tipo int e double.
 * O exemplo também demonstra como imprimir o conteúdo das variváveis na tela
 * @author tiago
 */
public class Comentarios {
    
    public static void main(String[] args) {
        
        int marticula = 1089, idade = 20; // declaração de variáveis do tipo inteiro
        double dolar = 2.62;
        /* As linhas seguintes enviam o conteúdo das
         variáveis para a tela */
        System.out.println("Matricula: " + marticula + " idade: " + idade + " dolar: " + dolar);
    }
    
}
